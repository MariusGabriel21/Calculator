namespace Calculator
{
    public class SimpleMath
    {
        public static double Adunare(double NumarulUnu, double NumarulDoi)
        {
            return NumarulUnu + NumarulDoi;
        }

        public static double Scadere(double NumarulUnu, double NumarulDoi)
        {
            return NumarulUnu - NumarulDoi;
        }
        public static double Inmultire(double NumarulUnu, double NumarulDoi)
        {
            return NumarulUnu * NumarulDoi;
        }
        public static double Impartire(double NumarulUnu, double NumarulDoi)
        {
            return NumarulUnu / NumarulDoi;
        }
    }
}
